  <body>
    <p>
      Tal vez el modelo del sistema neuro-m&#250;sculo-esquel&#233;tico NMS que mas impacto ha tenido recientemente, propuesto por~\cite{Geyer2010}, es un modelo desarrollado en MATLAB y SimMechanics, proponiendo una locomoci&#243;n coordinada de la caminata utilizando un conjunto de reflejos, que actuan sobre un conjunto de m&#250;sculos basados en el modelo de Hill propuesto en~\cite{Geyer2003}, donde se estudia la relaci&#243;n fuerza-longitud-velocidad del m&#250;sculo. Los m&#250;sculos ser oganizan, formando sinerg&#237;as musculares, de forma que permiten al sistema esquel&#233;tico evitar: hiperextenciones de las articulaciones, flexibilidad, evadir el choque con el piso en la etapa de balanceo y otras. Se utiliza algoritmos gen&#233;ticos GAs para la parametrizaci&#243;n del sistema NMS.
    </p>
    <p>
      
    </p>
    <p>
      Posterior a este modelo, Geyer continuo el modelo esta vez para un sistema tridimensional \cite{Song2015} a&#241;adiendo nuevos musculos y reflejos, en donde emergen distintos comportamientos de marcha en modo de caminata y corrida. El controlador es un sistema ger&#225;rquico compuesto por dos niveles, en el primero se almacenan y coordinan los reflejos y en el segundo se controla el posicionamiento del pie. El proceso de optimizaci&#243;n de los parametros es cambiado por otra t&#233;cnica evolutiva, denomicada \emph{Covariance Matrix Adpatation-Estartegia Evolutiva} CMA-ES~\cite{Hansen2006}.
    </p>
    <p>
      
    </p>
    <p>
      Paralelo al trabajo de~\cite{Geyer2010,Song2015}, Andr&#233; Seyfarth continuo desde ~\cite{Geyer2003}, con un trabajo sobre el modelo muscular y su importancia en la comuniacion de las se&#241;anles realimentadas y prealimentad&#225;s en la adaptaci&#243;n a un entorno cambiante o con cierto nivel de incertidumbre~\cite{Haeufle2012}. Dentro del canal de comunicaci&#243;n, las se&#241;ales aferentes provenientes de las fibras sensoriales son estudiadas en detenimiento para el caso de un movimiento peri&#243;dico de salto. Los husos neuromusculares como receptores sensoriales, llevan la informaci&#243;n propioceptiva al controlador, estos son: (1) Longitud y velocidad de alargamiento y contracci&#243;n del musculo y (2) los &#243;rganos tendinosos de Golgi llevan la informaci&#243;n de la fuerza. Este canal es estudiado concluyendo la importancia de cada una de estas se&#241;ales en la estabilidad y control del salto, adem&#225;s de especificar si la realimentacion es positiva o negativa.
    </p>
    <p>
      
    </p>
    <p>
      El siguiente paso de D.F.B Haeufle, al comprobar que las no-linealidades eran &#250;tilies en el modelo muscular de~\cite{Haeufle2012}, fu&#233; cuantificar la informaci&#243;n que flu&#237;a por el canal al mirar que tanto computo deb&#237;a realizar el controlador para lograr una accion especifica~\cite{Haeufle2014}, para ello utiliz&#243; \emph{la entrop&#237;a de la informaci&#243;n de Shannon}~\cite{MacKay2005}. Ya metido en el campo de la computaci&#243;n morfol&#243;gica compar&#243; tres modelos de robots saltadores simulados al mirar la complejidad del control en cada uno de los tres siguientes casos: (1) modelo no-lineal del m&#250;sculo de Hill, (2) modelo lineal del m&#250;sculo de Hill y (3) motor DC como actuador~\cite{Ghazi-Zahedi2015a}. Otros modelos de musculos basados en Hill son propuestos en~\cite{Schmitt2015} complementan mejor la comparaci&#243;n de un control real.
    </p>
    <p>
      
    </p>
    <p>
      Extendiendo el trabajo de~\cite{Geyer2010} y paralelo a~\cite{Song2015}, Auke Ijspeert, conocido por su trabajo en CPGs~\cite{Ijspeert2008}, muestra la necesidad de incluir los CPGs en la estructura puramente reflexiva de Geyer en~\cite{Dzeladini2014}. Aunque el modelo de Geyer es estable en presencia de perturbaciones, la modulaci&#243;n de la velocidad y el tama&#241;o del paso no son problemas sencillos. Al incluir los CPGs en el modelo NMS, la modulaci&#243;n la velocidad y paso de la marcha se tornan sencillas y adem&#225;s se muestra que los CPGs pueden ser utlizados como modelos predictivos del lazo sensoriomotor al ser capaces de reproducir las se&#241;ales aferentes generadas por una marcha estable. Esta capacidad predictiva de los CPGs ofrece una forma de comparar la importancia relativa de distintos circuitos o pathways de realimentacion. Como trabajos futuros se propone un estudio de co-evoluci&#243;n de las componentes de realimentaci&#243;n y prealimentaci&#243;n. Adicional se concluye que las se&#241;ales de los pathways pueden ser estudiadas mejor a trav&#233;s de las se&#241;ales de las motoneuronas formando se&#241;ales de baja dimensi&#243;n compuesta por cuatro primitivas motrices controlando con una mayor facilidad la velocidad, longitud de paso, transici&#243;n de marchas y la adaptaci&#243;n a pendientes cambiantes. La reduccion de dimensionalidad expresada en primitivas de movimiento, de nueve es estudiada por Ijspeert en~\cite{Sprowitz2014a}, donde se analiza el flujo de informaci&#243;n mediante PCA y se reduce a los cuatro primero componentes logrando un 95% de precisi&#243;n.
    </p>
    <p>
      
    </p>
    <p>
      Los CPGs codifican la informaci&#243;n mediante circuitos neuronales~\cite{Grillner1985}, que se pueden analizar mediante diferentes tipos de osciladores aunque el m&#225;s utilizado recientemente es el de Kuramoto. En el trabajo de~\cite{Dzeladini2014}, se utilizan ociladores morfados~\cite{Ajallooeian2013}. La implementaci&#243;n de este trabajo fue desarrollada en C++, escribiendo unas librerias para el sistema NMS y la optimizaci&#243;n de los par&#225;metros se llev&#243; acabo con un algoritmo multi-criterio, ya que siempre se emplearon entre dos o m&#225;s criterios. Se emplearon dos t&#233;cnicas, Ordenamiento Lexicografico para manejar el ordenamiento multiobejetivo de la funci&#243;n de fitness sobre el frente de pareto y la optimizaci&#243;n basada en enjambres de particulas PSO como estrategia de busqueda principal.
    </p>
    <p>
      
    </p>
    <p>
      Auke Ijspeert desarrolla un framework en~\cite{Ajallooeian2013}, dirigido al aprendizaje de la locomoci&#243;n, denomiado &quot;osciladores de fase no-lineales morfados&quot;. Esta familia de osciladores incluye a los CPGs y a las primitivas de movimiento din&#225;micas DMPs~\cite{Ijspeert2002,Ijspeert2013} desarrolladas por inicialmente por Stefan Schaal. Esta es otra de las caracteristicas del la encarnaci&#243;n de la inteligencia utilizando la computaci&#243;n morfologica, ya que este framework permite el dise&#241;o de sistemas din&#225;micos con distintos tipos de atractores. Sobre los Osciladores Morfados se construye una arquitectura Actor-Critica para el aprendizaje de la locomoci&#243;n, compuesto de CPGs y DMPs para movimientos periodicos y discretos~\cite{Li2013a,Li2014}.
